// в”Җв”Җв”Җ Board parser / serializer (thin wrappers around Board methods) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

// boardToString(board, toPlay?) вҖ” serialize board to ASCII; if toPlay ('в—Ҹ'/'в—Ӣ')
// is given, prepend it as the first line so parseBoard can recover it.
function boardToString(board, toPlay) {
  const body = board.toAscii();
  return toPlay ? toPlay + '\n' + body : body;
}

// parseBoard(str) вҖ” returns { size, stones, toPlay? }.
// toPlay is 'в—Ҹ' or 'в—Ӣ' if the string was produced with boardToString(board, toPlay).
function parseBoard(boardStr) {
  const lines = boardStr.trim().split('\n').map(r => r.trim());
  let toPlay;
  if (lines[0] === 'в—Ҹ' || lines[0] === 'в—Ӣ') {
    toPlay = lines.shift();
  }
  const result = Board.parse(lines.join('\n'));
  if (toPlay !== undefined) result.toPlay = toPlay;
  return result;
}

// в”Җв”Җв”Җ Position builder в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

function buildPosition(puzzle) {
  const { size, stones } = parseBoard(puzzle.board);
  const game = new Game(size, 0);
  const c = size >> 1;
  game.board.set(c, c, null);
  game.hash = 0n;
  game.moveCount = 0;
  game.current = puzzle.toPlay === 'в—Ҹ' ? 'black' : puzzle.toPlay === 'в—Ӣ' ? 'white' : puzzle.toPlay;
  game.consecutivePasses = 0;
  game.koFlag = null;
  for (const [x, y, color] of stones) {
    game.board.set(x, y, color);
    game.hash ^= ZOBRIST[y][x][color];
  }
  game.board._rebuildGroups();
  return game;
}

