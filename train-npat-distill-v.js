#!/usr/bin/env node
'use strict';

// train-npat-distill-v.js — value-based distillation of an oracle policy
// (vlibpat) into npat (3×3 centered shape + tactical features).
//
// At each oracle step:
//   1. For every legal move, compute the oracle's V(s_after) (P(BLACK wins)).
//   2. Convert to current-player logits, divide by T, softmax → target distribution.
//   3. Soft-target cross-entropy gradient:
//        Δw_k = lr · Σ_i (π_target(i) - π_npat(i)) · feat_k(i)
//   4. Play the oracle's argmax move (and continue).
//
// Position generator: oracle plays both sides; with probability epsilon a
// uniform-random legal move is played instead (positions explored, not trained on).

const path = require('path');
const fs   = require('fs');

const { Game2, BLACK, PASS } = require('./game2.js');
const { Game3 } = require('./game3.js');
const NPat = require('./npat-lib.js');
const Util = require('./util.js');
const vlibpat = require('./vlibpat.js');

// ── Arguments ─────────────────────────────────────────────────────────────────

const opts = Util.parseArgs(process.argv.slice(2), ['help', 'normalize-v']);
if (opts.help) {
  console.log(`Usage: node train-npat-distill-v.js [options]
  --size N           board size (default 9)
  --lr F             learning rate (default 0.05)
  --epsilon F        random-move probability (default 0.1; not trained on)
  --temperature F    softmax temperature on oracle logits (default 1.0)
  --normalize-v      use min-max-scaled V (per position) instead of log-odds (default off)
  --ema-alpha F      Polyak EMA decay per game (default 0.999)
  --oracle-weights P vlibpat weights file (default out/vlibpat-9-2L3L3NL-onpol70-9.js)
  --print-every N    print cadence in games (default 100)
  --limit N          stop after N games (default: forever)
  --load PATH        resume from saved weights
  --save PATH        where to save (default out/npat-distill-v-<rand>.js)
`);
  process.exit(0);
}
const TRAIN_SIZE     = parseInt(opts['train-size'] || opts.size || '9', 10);
const LR             = parseFloat(opts.lr || '0.05');
const EPSILON        = Math.min(parseFloat(opts.epsilon || '0.1'), 0.9999);
const TEMPERATURE    = parseFloat(opts.temperature || '1.0');
const NORMALIZE_V    = !!opts['normalize-v'];
const EMA_ALPHA      = parseFloat(opts['ema-alpha'] || '0.999');
const ORACLE_WEIGHTS = opts['oracle-weights'] || 'out/vlibpat-9-2L3L3NL-onpol70-9.js';
const LIMIT_GAMES    = opts.limit !== undefined ? parseInt(opts.limit, 10) : 0;
const SAVE_PATH      = opts.save || `out/npat-distill-v-${Math.random().toString(36).slice(2, 10)}.js`;
const LOAD_PATH      = opts.load || null;

const USE_33C = true;
const USE_E   = false;
const USE_TACT = true;

// ── Weights ──────────────────────────────────────────────────────────────────

let weights = NPat.createWeights({ useTactical: USE_TACT, use33c: USE_33C, useE: USE_E });
let totalUpdates = 0;

// ── Persistence ──────────────────────────────────────────────────────────────

function saveWeights(filePath, w) {
  const useEMA = w.valsEMAInit;
  const source = useEMA ? w.valsEMA : w.vals;
  const parts = [];
  for (const [rawId, denseIdx] of w.map) {
    parts.push(`[${JSON.stringify(rawId)},${+source[denseIdx].toFixed(6)}]`);
  }
  const list = '[' + parts.join(',') + ']';
  const src = [
    "'use strict';",
    '// Auto-generated by train-npat-distill-v.js — do not edit by hand.',
    `const npatModel = {`,
    `  totalUpdates: ${Math.round(totalUpdates)},`,
    `  tactStoneLimit: ${NPat.TACT_STONE_LIMIT},`,
    `  emaAlpha: ${+EMA_ALPHA.toFixed(6)},`,
    `  weightsAreEMA: ${useEMA},`,
    `  weights: new Map(${list}),`,
    `};`,
    "if (typeof module !== 'undefined') module.exports = npatModel;",
    "else window.npatModel = npatModel;",
  ].join('\n') + '\n';
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, src);
}

function loadWeights(filePath) {
  delete require.cache[require.resolve(path.resolve(filePath))];
  const raw = require(path.resolve(filePath));
  if (raw.tactStoneLimit !== undefined && raw.tactStoneLimit !== NPat.TACT_STONE_LIMIT) {
    throw new Error(
      `TACT_STONE_LIMIT mismatch: file=${raw.tactStoneLimit} runtime=${NPat.TACT_STONE_LIMIT}`
    );
  }
  const w = NPat.createWeights({
    initialCapacity: Math.max(1024, raw.weights.size | 0),
    useTactical: USE_TACT, use33c: USE_33C, useE: USE_E,
  });
  for (const [rawId, val] of raw.weights) {
    const idx = NPat.internWeight(w, rawId);
    w.vals[idx] = val;
    w.valsEMA[idx] = val;
  }
  w.valsEMAInit = true;
  return { weights: w, totalUpdates: raw.totalUpdates ?? 0 };
}

if (LOAD_PATH) {
  const loaded = loadWeights(LOAD_PATH);
  weights = loaded.weights;
  totalUpdates = loaded.totalUpdates;
  console.log(`Loaded ${weights.size} weights from ${LOAD_PATH}  (totalUpdates=${totalUpdates})`);
}

// ── Oracle ───────────────────────────────────────────────────────────────────

const oracleModel = vlibpat.loadWeights(path.resolve(ORACLE_WEIGHTS));
console.log(`Oracle: ${ORACLE_WEIGHTS} (${oracleModel.weights.size} weights, specs ${JSON.stringify(oracleModel.specs)})`);
console.log(`lr=${LR}  epsilon=${EPSILON}  temperature=${TEMPERATURE}  normalize-v=${NORMALIZE_V}  ema-alpha=${EMA_ALPHA}  train-size=${TRAIN_SIZE}`);
console.log(`Out: ${SAVE_PATH}${LOAD_PATH ? `  (resumed from ${LOAD_PATH})` : ''}`);
console.log();
console.log([
  'game'.padStart(6),
  'elapsedS'.padStart(8),
  'tGameMs'.padStart(8),
  'weights'.padStart(9),
  'avglen'.padStart(7),
  'match%'.padStart(7),
  'avg|w|'.padStart(7),
].join('  '));

// ── Soft-target gradient: Δw_k = lr · Σ_i (π_target(i) - π_npat(i)) · feat_k(i) ──

function softTargetUpdate(state, targetProbs, weights, lr) {
  const n = state.count;
  if (n === 0) return 0;
  const probs = state.probs;
  const tact  = state.tact;
  const vals  = weights.vals;
  const delta = weights.delta;
  const tIds  = weights.tactIds;
  const touched = state.touched;
  const cfg   = weights.cfg;
  const patIds33c = cfg.use33c ? state.patIds33c : null;
  const patIdsE   = cfg.useE   ? state.patIdsE   : null;
  const N_TACT = NPat.N_TACT_SLOTS;

  let tc = 0;
  for (let i = 0; i < n; i++) {
    const r = lr * (targetProbs[i] - probs[i]);
    if (r === 0) continue;
    if (patIds33c) {
      const idx = patIds33c[i];
      touched[tc++] = idx;
      delta[idx] += r;
    }
    if (patIdsE) {
      const idx = patIdsE[i];
      touched[tc++] = idx;
      delta[idx] += r;
    }
  }
  for (let i = 0; i < tc; i++) {
    const idx = touched[i];
    const d = delta[idx];
    if (d !== 0) {
      vals[idx] += d;
      delta[idx] = 0;
    }
  }
  if (cfg.useTactical !== false) {
    for (let k = 0; k < N_TACT; k++) {
      let acc = 0;
      for (let i = 0; i < n; i++) {
        acc += lr * (targetProbs[i] - probs[i]) * tact[i * N_TACT + k];
      }
      vals[tIds[k]] += acc;
    }
  }
  return tc + (cfg.useTactical !== false ? N_TACT : 0);
}

// ── Oracle target distribution (current-player perspective) ──────────────────

function computeOracleTarget(game, state) {
  const n = state.count;
  if (n === 0) return null;
  const isBlack = game.current === BLACK;
  const logits = new Float64Array(n);

  if (NORMALIZE_V) {
    // Min-max normalize current-player V across moves to [0, 1], then use as logits.
    const Vcp = new Float64Array(n);
    let vMin = Infinity, vMax = -Infinity;
    for (let i = 0; i < n; i++) {
      const g = game.clone();
      g.play(state.moves[i]);
      const V = vlibpat.evaluateFeatures(
        vlibpat.extractFeatures(g, oracleModel.preparedSpecs),
        oracleModel.weights,
      );
      const v = isBlack ? V : (1 - V);
      Vcp[i] = v;
      if (v < vMin) vMin = v;
      if (v > vMax) vMax = v;
    }
    const range = vMax - vMin;
    if (range < 1e-12) {
      for (let i = 0; i < n; i++) logits[i] = 0;
    } else {
      for (let i = 0; i < n; i++) logits[i] = (Vcp[i] - vMin) / range / TEMPERATURE;
    }
  } else {
    for (let i = 0; i < n; i++) {
      const g = game.clone();
      g.play(state.moves[i]);
      const V = vlibpat.evaluateFeatures(
        vlibpat.extractFeatures(g, oracleModel.preparedSpecs),
        oracleModel.weights,
      );
      const eps = 1e-7;
      const Vc = Math.max(eps, Math.min(1 - eps, V));
      let logit = Math.log(Vc / (1 - Vc));
      if (!isBlack) logit = -logit;
      logits[i] = logit / TEMPERATURE;
    }
  }

  let maxL = -Infinity;
  for (let i = 0; i < n; i++) if (logits[i] > maxL) maxL = logits[i];
  let sum = 0;
  for (let i = 0; i < n; i++) { logits[i] = Math.exp(logits[i] - maxL); sum += logits[i]; }
  const inv = 1 / sum;
  for (let i = 0; i < n; i++) logits[i] *= inv;
  return logits;
}

function argmaxIndex(arr, n) {
  let best = 0, bestV = arr[0];
  for (let i = 1; i < n; i++) if (arr[i] > bestV) { bestV = arr[i]; best = i; }
  return best;
}

// ── Training game ────────────────────────────────────────────────────────────

function trainGame(N) {
  const game = new Game2(N, false);
  const game3 = new Game3(N);
  const maxMoves = N * N * 4;
  const tStart = Date.now();
  const state = NPat.createState(N);

  let moves = 0, trained = 0, matched = 0;

  while (!game.gameOver && moves < maxMoves) {
    let mv;
    if (EPSILON > 0 && Math.random() < EPSILON) {
      NPat.policyMove(game, state, weights, Math, game3);
      mv = state.count === 0 ? PASS : state.moves[Math.floor(Math.random() * state.count)];
    } else {
      NPat.policyMove(game, state, weights, Math, game3);  // fills state.probs
      if (state.count > 0) {
        const target = computeOracleTarget(game, state);
        const npatBest = argmaxIndex(state.probs, state.count);
        const oracleBest = argmaxIndex(target, state.count);
        if (npatBest === oracleBest) matched++;
        totalUpdates += softTargetUpdate(state, target, weights, LR) || 0;
        trained++;
        mv = state.moves[oracleBest];
      } else {
        mv = PASS;
      }
    }
    game.play(mv);
    game3.play(mv);
    moves++;
  }

  return { elapsedMs: Date.now() - tStart, moves, trained, matched };
}

// ── Main loop ────────────────────────────────────────────────────────────────

const PRINT_EVERY = parseInt(opts['print-every'] || '100',  10);

const t0 = Date.now();
let g = 0;

let movesAcc = 0, trainedAcc = 0, matchedAcc = 0;
let elapsedMsAcc = 0;
let lastPrintG = 0;

let limitReached = false;

while (true) {
  g++;
  const r = trainGame(TRAIN_SIZE);
  movesAcc += r.moves;
  trainedAcc += r.trained;
  matchedAcc += r.matched;
  elapsedMsAcc += r.elapsedMs;
  NPat.applyEMA(weights, EMA_ALPHA);

  if (LIMIT_GAMES > 0 && g >= LIMIT_GAMES) limitReached = true;

  if (g % PRINT_EVERY === 0 || limitReached) {
    let wAbsSum = 0, wNonZero = 0;
    const vals = weights.vals;
    const wN   = weights.size;
    for (let i = 0; i < wN; i++) {
      const a = Math.abs(vals[i]);
      if (a === 0) continue;
      wNonZero++;
      wAbsSum += a;
    }
    const wAvg = wNonZero > 0 ? wAbsSum / wNonZero : 0;

    const cycleN = g - lastPrintG;
    const cycleAvgGameMs = cycleN > 0 ? elapsedMsAcc / cycleN : 0;
    const elapsedS = Math.floor((Date.now() - t0) / 1000);
    const matchPct = trainedAcc > 0 ? 100 * matchedAcc / trainedAcc : 0;
    const avgLen = cycleN > 0 ? movesAcc / cycleN : 0;

    console.log([
      `${g}`.padStart(6),
      `${elapsedS}`.padStart(8),
      `${cycleAvgGameMs.toFixed(1)}`.padStart(8),
      `${wNonZero}`.padStart(9),
      `${avgLen.toFixed(1)}`.padStart(7),
      `${matchPct.toFixed(1)}%`.padStart(7),
      `${wAvg.toFixed(4)}`.padStart(7),
    ].join('  '));

    saveWeights(SAVE_PATH, weights);

    movesAcc = 0; trainedAcc = 0; matchedAcc = 0; elapsedMsAcc = 0;
    lastPrintG = g;

    if (limitReached) break;
  }
}

if (LIMIT_GAMES > 0) {
  console.log(`Reached --limit ${LIMIT_GAMES} games — saved ${SAVE_PATH}`);
}
