#!/usr/bin/env node
'use strict';

// train-hpatterns.js — learn pattern weights via logistic TD(2) self-play.
//
// Value function (absolute, P(BLACK wins)):
//   V(s) = σ( Σ  polarity_i · w[key_i] )
//
// Update rule — logistic TD, 2-step lookahead (same player to move):
//   Δw_k = (LR / n_features) · (target − V) · polarity_k
//   target = V(s_{t+2})   for t+2 < game length  [bootstrap]
//          = 1 / 0.5 / 0  for terminal            [BLACK wins / draw / WHITE wins]
//
// Features: hierarchical pattern hashes, all sizes 2×2 … maxSize×maxSize.
// Canonicalised via algorithmic D4 + color-flip; hash collisions accepted.
//
// Runs indefinitely (Ctrl-C to stop).  Weights are saved at every print.

const path = require('path');
const fs   = require('fs');
const { Game2, BLACK, PASS } = require('./game2.js');
const { createModel, extractFeatures, evaluateFeatures } = require('./hpatterns.js');
const Util = require('./util.js');

// ── Arguments ─────────────────────────────────────────────────────────────────

const opts       = Util.parseArgs(process.argv.slice(2));
const TRAIN_SIZE = parseInt(opts['train-size']  || opts.size || '9',  10);
const EVAL_SIZE  = parseInt(opts['eval-size']   || opts.size || opts['train-size'] || '9',  10);
const SAVE_PATH  = opts.save  || `out/hpatterns-${Math.random().toString(36).slice(2, 10)}.js`;
const LOAD_PATH  = opts.load  || null;
const EVAL_AGENT = opts.eval  || 'prod';
const EPSILON    = Math.min(parseFloat(opts.epsilon   || '0.1'),  0.9999);
const LR         = parseFloat(opts.lr               || '0.3');
const MOMENTUM   = parseFloat(opts.momentum         || '0.0');
// spec: "size:maxStones" pairs, e.g. "2:4,3:8,4:16". Sizes absent from spec are not extracted.
const SPEC_RAW = opts.spec || '2:4';
const SPEC = {};
for (const part of SPEC_RAW.split(',')) {
  const [k, v] = part.split(':');
  SPEC[parseInt(k, 10)] = parseInt(v, 10);
}
const MAX_SIZE   = Math.max(...Object.keys(SPEC).map(Number));
const MAX_STONES = SPEC;

// ── Model ─────────────────────────────────────────────────────────────────────

let model = createModel(MAX_STONES, MAX_SIZE);
const velocity = new Map();  // SGD momentum

// ── Persistence ───────────────────────────────────────────────────────────────

function saveModel(filePath, m) {
  const maxStonesStr = JSON.stringify(m.maxStones);
  const weightsStr = '[' + [...m.weights].map(([k, v]) => `[${k},${+v.toFixed(6)}]`).join(',') + ']';
  const src = [
    "'use strict';",
    '// Auto-generated by train-hpatterns.js — do not edit by hand.',
    `const hpatternsModel = {`,
    `  maxStones: ${maxStonesStr},`,
    `  maxSize: ${m.maxSize === Infinity ? 'Infinity' : m.maxSize},`,
    `  weights: new Map(${weightsStr}),`,
    `};`,
    "if (typeof module !== 'undefined') module.exports = hpatternsModel;",
    "else window.hpatternsModel = hpatternsModel;",
  ].join('\n') + '\n';
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, src);
}

function loadModel(filePath) {
  const raw = require(path.resolve(filePath));
  return createModel(raw.maxStones, raw.maxSize === Infinity ? Infinity : raw.maxSize,
    new Map(raw.weights));
}

// createModel doesn't accept an initial weights map; patch it in.
function createModelWithWeights(maxStones, maxSize, weights) {
  const m = createModel(maxStones, maxSize);
  m.weights = weights;
  return m;
}

// ── Training helpers ──────────────────────────────────────────────────────────

function absoluteOutcome(game) {
  return game.calcWinner() === BLACK ? 1 : 0;
}

function tdUpdate(features, target, lr) {
  const n = features.count;
  if (n === 0) return;
  const perFeature = (target - features.val) / n;
  const { keys, pols } = features;
  if (MOMENTUM === 0) {
    const step = lr * perFeature;
    for (let i = 0; i < n; i++) {
      const k = keys[i];
      model.weights.set(k, (model.weights.get(k) ?? 0) + pols[i] * step);
    }
  } else {
    for (let i = 0; i < n; i++) {
      const k   = keys[i];
      const g   = pols[i] * perFeature;
      const vel = MOMENTUM * (velocity.get(k) ?? 0) + g;
      velocity.set(k, vel);
      model.weights.set(k, (model.weights.get(k) ?? 0) + lr * vel);
    }
  }
}

// ── 1-ply search ──────────────────────────────────────────────────────────────

function search1ply(game) {
  const area    = game.N * game.N;
  const isBlack = game.current === BLACK;
  let bestMove  = PASS;
  let bestVal   = isBlack ? -Infinity : Infinity;

  for (let coord = 0; coord < area; coord++) {
    if (!game.isLegal(coord) || game.isTrueEye(coord)) continue;
    const f = extractFeatures(game, model, true, coord);
    f.val = evaluateFeatures(f, model.weights);
    if (isBlack ? f.val > bestVal : f.val < bestVal) {
      bestVal  = f.val;
      bestMove = coord;
    }
  }

  // Consider passing late in the game or after opponent passed.
  if (game.consecutivePasses > 0 || game.emptyCount < area / 2) {
    const fPass = extractFeatures(game, model);
    fPass.val = evaluateFeatures(fPass, model.weights);
    if (isBlack ? fPass.val >= bestVal : fPass.val <= bestVal) {
      bestMove = PASS;
    }
  }

  return bestMove;
}

// ── Self-play training ────────────────────────────────────────────────────────

function trainGame(N) {
  const game     = new Game2(N, false);
  const maxMoves = N * N * 4;
  const tStartMs = Date.now();

  let prev2 = null, prev1 = null;
  let moves = 0;
  let correct = 0;
  const vals = [];

  while (!game.gameOver && moves < maxMoves) {
    const f = extractFeatures(game, model);
    f.val = evaluateFeatures(f, model.weights);
    vals.push(f.val);

    if (prev2 !== null) tdUpdate(prev2, f.val, LR);

    prev2 = prev1;
    prev1 = { keys: f.keys.slice(0, f.count), pols: f.pols.slice(0, f.count), count: f.count, val: f.val };

    game.play(Math.random() < EPSILON ? game.randomLegalMove() : search1ply(game));
    moves++;
  }

  const outcome = absoluteOutcome(game);

  if (prev2 !== null) tdUpdate(prev2, outcome, LR);
  if (prev1 !== null) tdUpdate(prev1, outcome, LR);

  for (const v of vals) if ((v >= 0.5) === (outcome === 1)) correct++;

  return { elapsedMs: Date.now() - tStartMs, moves, correct, nVals: vals.length };
}

// ── Evaluation against a reference agent ─────────────────────────────────────

function evalVsReference(N, refGetMove, nGames) {
  const results = [];
  for (let g = 0; g < nGames; g++) {
    const policyIsBlack = (g % 2 === 0);
    const game     = new Game2(N, false);
    const maxMoves = N * N * 4;
    let   moves    = 0;

    while (!game.gameOver && moves++ < maxMoves) {
      let idx;
      if ((game.current === BLACK) === policyIsBlack) {
        idx = search1ply(game);
      } else {
        const mv = refGetMove(game);
        idx = mv.move !== undefined ? mv.move : PASS;
      }
      game.play(idx);
    }

    const winner = game.calcWinner();
    results.push((winner === BLACK) === policyIsBlack ? 1 : 0);
  }
  return results;
}

// ── CLI setup ─────────────────────────────────────────────────────────────────

const { getMove: evalGetMove } = require(path.join(__dirname, 'ai', EVAL_AGENT + '.js'));

if (LOAD_PATH) {
  if (fs.existsSync(LOAD_PATH)) {
    const raw = require(path.resolve(LOAD_PATH));
    model = createModelWithWeights(raw.maxStones, raw.maxSize, new Map(raw.weights));
    console.log(`Loaded ${model.weights.size} weights from ${LOAD_PATH}`);
  } else {
    console.warn(`Warning: --load file not found: ${LOAD_PATH}`);
  }
}

console.log(`LR=${LR}  momentum=${MOMENTUM}  epsilon=${EPSILON}  train-size=${TRAIN_SIZE}  eval-size=${EVAL_SIZE}  ref=${EVAL_AGENT}`);
console.log(`spec=${SPEC_RAW}`);
console.log(`Out: ${SAVE_PATH}${LOAD_PATH ? `  (resumed from ${LOAD_PATH})` : ''}`);
console.log();

const col = n => String(n).padStart;
console.log([
  'game'   .padStart(7),
  'elapsed'.padStart(8),
  'tGameMs'.padStart(7),
  'weights'.padStart(8),
  'canonKs'.padStart(8),
  'win%'   .padStart(6) + '(' + 'n'.padStart(3) + ')',
  'winAvg%'.padStart(7),
  'avglen' .padStart(6),
  'acc%'   .padStart(5),
  'avg|w|' .padStart(7),
  'rms(w)' .padStart(7),
  'max|w|' .padStart(7),
  'tTrain' .padStart(7),
  'tTest'  .padStart(6),
  'turnMs' .padStart(6),
].join('  '));

// ── Main loop ─────────────────────────────────────────────────────────────────

const t0 = Date.now();
let nextPrintAt = t0 + 1000;
let g = 0;
let totalMoves = 0;
let intervalGames = 0, intervalMoves = 0;
let intervalCorrect = 0, intervalNVals = 0;
let totalCorrect = 0, totalNVals = 0;
let moveElapsedMs = 0;
let intervalTrainMs = 0;
const evalHistory = [];

while (true) {
  g++;
  const { moves, elapsedMs, correct, nVals } = trainGame(TRAIN_SIZE);
  totalMoves      += moves;
  intervalGames++;
  intervalMoves   += moves;
  intervalCorrect += correct;
  intervalNVals   += nVals;
  totalCorrect    += correct;
  totalNVals      += nVals;
  moveElapsedMs   += elapsedMs;
  intervalTrainMs += elapsedMs;

  if (Date.now() >= nextPrintAt) {
    const tTestStart = Date.now();
    const batch = [];
    while (true) {
      for (const r of evalVsReference(EVAL_SIZE, evalGetMove, 2)) batch.push(r);
      const tMs = Date.now() - tTestStart;
      if (tMs > 0.3 * intervalTrainMs || batch.length >= 998) break;
    }
    for (const r of batch) evalHistory.push(r);

    const latestWR  = batch.reduce((s, r) => s + r, 0) / batch.length;
    const half      = Math.max(1, Math.floor(evalHistory.length / 2));
    const avgWR     = evalHistory.slice(-half).reduce((s, r) => s + r, 0) / half;
    const avgLen    = (intervalMoves / intervalGames).toFixed(1);
    const tGameMs   = (intervalTrainMs / intervalGames).toFixed(1);
    const avgAcc    = (100 * totalCorrect / totalNVals).toFixed(1);
    const tpMove    = moveElapsedMs / totalMoves;

    intervalGames   = 0;
    intervalMoves   = 0;
    intervalCorrect = 0;
    intervalNVals   = 0;

    // Count unique canonKeys referenced in weights.
    let wAbsSum = 0, wSqSum = 0, wAbsMax = 0;
    for (const w of model.weights.values()) {
      const a = Math.abs(w);
      wAbsSum += a; wSqSum += w * w;
      if (a > wAbsMax) wAbsMax = a;
    }
    const ws  = model.weights.size;
    const wAvg = ws > 0 ? wAbsSum / ws : 0;
    const wRms = ws > 0 ? Math.sqrt(wSqSum / ws) : 0;

    const tTestMs   = Date.now() - tTestStart;
    const elapsedS  = ((Date.now() - t0) / 1000).toFixed(0);
    const nextMs    = Date.now() - t0;
    const tTrainStr = (intervalTrainMs / 1000).toFixed(1) + 's';
    intervalTrainMs = 0;

    console.log([
      String(g)                                       .padStart(7),
      elapsedS                                        .padStart(8),
      tGameMs                                         .padStart(7),
      String(ws)                                      .padStart(8),
      String(model.canonMap.size)                     .padStart(8),
      ((100 * latestWR).toFixed(1) + '%')             .padStart(6) + '(' + String(batch.length).padStart(3) + ')',
      ((100 * avgWR).toFixed(1) + '%')                .padStart(7),
      avgLen                                          .padStart(6),
      (avgAcc + '%')                                  .padStart(5),
      wAvg.toFixed(3)                                 .padStart(7),
      wRms.toFixed(3)                                 .padStart(7),
      wAbsMax.toFixed(3)                              .padStart(7),
      tTrainStr                                       .padStart(7),
      ((tTestMs / 1000).toFixed(1) + 's')             .padStart(6),
      tpMove.toFixed(1)                               .padStart(6),
    ].join('  '));

    saveModel(SAVE_PATH, model);
    nextPrintAt = t0 + Math.round(nextMs * 1.4);
  }
}
