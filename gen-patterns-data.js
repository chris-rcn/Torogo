#!/usr/bin/env node
'use strict';

// Usage:
//   node gen-patterns-data.js --file <path/to/patterns.csv> > patdata.js
//
// Reads output of mine-pats-selection.js (hash,seen,selected,selectedWeightSum).
// Computes pairwise win rate = selectedWeightSum / (selectedWeightSum + seen - selected).
// Map values are [ratio, seen] pairs.

const fs   = require('fs');
const path = require('path');

const args = process.argv.slice(2);

function flag(name) {
  const i = args.indexOf(name);
  return i !== -1 ? args[i + 1] : null;
}

const inputFile     = flag('--file');
const minEncounters = parseInt(flag('--min-encounters') ?? '2', 10);

if (!inputFile) {
  console.error('Usage: node gen-patterns-data.js --file <patterns.csv> [--min-encounters <n>]');
  process.exit(1);
}

const lines = fs.readFileSync(inputFile, 'utf8').trim().split('\n');
const entries = [];
for (const line of lines) {
  if (!line.trim()) continue;
  const [rawHash, rawSeen, rawSelected, rawWeightSum] = line.split(',');
  const hash              = parseInt(rawHash,     10);
  const seen              = parseInt(rawSeen,     10);
  const selected          = parseInt(rawSelected, 10);
  const selectedWeightSum = parseInt(rawWeightSum, 10);
  const losses            = seen - selected;
  const total             = selectedWeightSum + losses;
  if (total >= minEncounters)
    entries.push([hash, selectedWeightSum / total, total]);
}

const body = entries.map(([h, r, c]) => `[${h},[${r},${c}]]`).join(',');
process.stdout.write(`'use strict';
// Auto-generated by gen-patterns-data.js from ${path.basename(inputFile)} — do not edit by hand.
const patternTable = new Map([${body}]);
if (typeof module !== 'undefined') module.exports = patternTable;
else window.patternTable = patternTable;
`);
