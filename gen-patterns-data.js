#!/usr/bin/env node
'use strict';

// Usage:
//   node gen-patterns-data.js --file <path/to/patterns.csv> > patdata.js

const fs   = require('fs');
const path = require('path');

const args = process.argv.slice(2);

function flag(name) {
  const i = args.indexOf(name);
  return i !== -1 ? args[i + 1] : null;
}

const inputFile     = flag('--file');
const minEncounters = parseInt(flag('--min-encounters') ?? '2', 10);

if (!inputFile) {
  console.error('Usage: node gen-patterns-data.js --file <patterns.csv> [--min-encounters <n>]');
  process.exit(1);
}

const lines = fs.readFileSync(inputFile, 'utf8').trim().split('\n');
const entries = [];
for (const line of lines) {
  if (!line.trim()) continue;
  const [rawHash, rawRatio, rawCount] = line.split(',');
  const hash  = parseInt(rawHash,  10);
  const ratio = parseFloat(rawRatio);
  const count = parseInt(rawCount, 10);
  if (count >= minEncounters)
    entries.push([hash, ratio]);
}

const body = entries.map(([h, r]) => `[${h},${r}]`).join(',');
process.stdout.write(`'use strict';
// Auto-generated by gen-patterns-data.js from ${path.basename(inputFile)} — do not edit by hand.
const patternTable = new Map([${body}]);
if (typeof module !== 'undefined') module.exports = patternTable;
else window.patternTable = patternTable;
`);
